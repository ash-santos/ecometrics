    fun getProvinceData(regionName: String): List<String> {

        val regionIndex = regions.find { it.name == regionName}?.id

        return provinces.filter { it.regionId == regionIndex}
            .sortedBy { it.name }
            .map { it.name }
    }

    fun getProvinceData(): List<String> {
        return provinces.sortedBy { it.name }
            .map { it.name }
    }

    fun getMunicipalityData(regionName: String, provinceName: String): List<String> {
        val regionIndex = regions.find { it.name == regionName }?.id
        val provinceIndex = provinces.find { it.name == provinceName }?.id

        return municipalities
            .filter { it.regionId == regionIndex && it.provinceId == provinceIndex }
            .map { it.name }
    }


